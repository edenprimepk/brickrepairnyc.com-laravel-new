                <p>Repointing is a critical step in preserving the longevity, appearance, and structural integrity of brickwork. But before that fresh layer of mortar is applied, another crucial process often comes into play: brick grinding. For many property owners, especially in older buildings across NYC, the question comes up: <strong>Do I really need brick grinding before repointing?</strong> This guide walks through when it’s necessary and why it matters.</p>

                <h3>What Is Brick Grinding?</h3>
                <p>Brick grinding is the process of removing the old, deteriorated mortar from between brick joints before repointing begins. It’s done using specialized angle grinders or masonry saws with diamond blades that can precisely cut through mortar without damaging the surrounding bricks.</p>
                <p>The goal is to remove mortar to a sufficient depth, usually about 3/4 inch to 1 inch, so that the new mortar can bond properly. Grinding ensures that the repointing isn’t just cosmetic but provides a strong, lasting seal between bricks.</p>

                <h3>When Is Brick Grinding Necessary?</h3>
                <p>Following are some of the signs when it’s become necessary to grind the bricks:</p>
                <ul>
                  <li><strong>When the Old Mortar Is Cracked, Loose, or Powdery:</strong> If mortar is visibly crumbling or easily flakes off when touched, it has likely lost its binding strength. Grinding ensures that all loose material is removed so the new mortar can fully adhere.</li>
                  <li><strong>During Historic or Large-Scale Repointing Projects:</strong> For historic buildings or major repointing efforts, grinding is essential to maintain long-term structural integrity. Simply patching over old mortar is never advisable for these cases, especially in NYC, where buildings often date back over a century.</li>
                  <li><strong>When Mortar Was Previously Improperly Repaired:</strong> In some cases, inappropriate materials (like Portland cement instead of lime mortar) were used in earlier repairs. Grinding helps fully remove the incompatible material so the correct mix can be used during repointing.</li>
                  <li><strong>To Meet NYC Building Code or Landmark Requirements:</strong> In regulated areas such as historic districts or buildings overseen by the NYC Landmarks Preservation Commission, brick grinding may be required before repointing. Failing to meet these standards can lead to rejected work or mandatory re-dos.</li>
                </ul>

                <h3>Why Is Brick Grinding So Important?</h3>
                <p>Skipping the grinding step might save time in the short term, but it introduces long-term risks:</p>
                <ul>
                  <li><strong>Better Bonding:</strong> New mortar cannot bond well with loose or deteriorated material. Grinding ensures clean, stable joints that allow proper adhesion and strength.</li>
                  <li><strong>Increased Lifespan:</strong> Properly ground and repointed joints can last 20–30 years or more, depending on the material and exposure. Without grinding, repointing might fail in under a decade.</li>
                  <li><strong>Reduced Risk of Water Infiltration:</strong> Poor adhesion leads to gaps where water can enter. In NYC’s freeze-thaw cycles, trapped water expands and contracts, cracking bricks and mortar. Grinding helps eliminate these hidden vulnerabilities.</li>
                </ul>

                <h3>Is Grinding Always Required?</h3>
                <p>Not necessarily. In cases where:</p>
                <ul>
                  <li>Only minor touch-up pointing is needed</li>
                  <li>The original mortar is still structurally sound</li>
                  <li>The building is newer or has been well-maintained</li>
                </ul>
                <p>A skilled mason might be able to perform repointing with light chipping or hand-tool removal instead of full grinding. However, this is the exception, not the rule, especially in older NYC buildings exposed to decades of weathering.</p>

                <h3>Conclusion</h3>
                <p>In most cases, especially across NYC’s aged housing stock and brownstones, grinding is a crucial prerequisite to successful, long-lasting repointing. It might seem like an extra step, but it directly affects the durability and appearance of the finished work.</p>
                <p>Whether you’re preserving a pre-war walk-up or maintaining a commercial facade, understanding the role of brick grinding helps ensure your repointing project is done right the first time.</p>
